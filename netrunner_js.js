
//////////////////////////////////////// BUTTON TOGGLERS ////////////////////////

function toggleHelp() {
    let x = document.getElementById("help-section");
    if (x.style.display === "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}

